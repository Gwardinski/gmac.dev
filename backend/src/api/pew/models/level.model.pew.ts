export type LevelTiles = LevelTileRow[];

export class LevelClass {
  constructor(public level: LevelTiles) {
    this.level = level;
    this.playerSpawnPoints = this.findPlayerSpawnPoints();
    this.itemSpawnPoints = this.findItemSpawnPoints();
  }

  public playerSpawnPoints: { x: number; y: number }[];
  public itemSpawnPoints: { x: number; y: number }[];
  public itemSpawnInterval: number = 20000;
  public itemLastSpawnTimestamp: number = Date.now();
  public maxItems: number = 1;

  private findPlayerSpawnPoints(): { x: number; y: number }[] {
    const spawnPoints: { x: number; y: number }[] = [];

    this.level.forEach((row, rowIndex) => {
      row.forEach((tile, colIndex) => {
        if (tile === 3) {
          spawnPoints.push({
            x: colIndex * GRID_SIZE,
            y: rowIndex * GRID_SIZE,
          });
        }
      });
    });

    return spawnPoints;
  }

  private findItemSpawnPoints(): { x: number; y: number }[] {
    const spawnPoints: { x: number; y: number }[] = [];

    this.level.forEach((row, rowIndex) => {
      row.forEach((tile, colIndex) => {
        if (tile === 4) {
          spawnPoints.push({
            x: colIndex * GRID_SIZE,
            y: rowIndex * GRID_SIZE,
          });
        }
      });
    });

    return spawnPoints;
  }
}

